# RAISING A NUMBER TO A POWER >> pow(base, exp)
x = pow(2, 2)
y = pow(4, 3)
print(x)
print(y)
# *************************

# GETTING ABSOLUTE VALUE 
a = abs(-4)
b = abs(-3.21)
print(a)
print(b)
# *************************

# GETTING QUOTIENT AND REMINDER
z = divmod(5, 2)
print(z)


#----------------------------------------------------
#     OPERATOR     |     EXAMPLE     |   SAME AS    |
#----------------------------------------------------
#     =            |     x = 5       |   x = 5      |
#----------------------------------------------------
#     +=           |     x += 5      |   x = x + 5  |
#----------------------------------------------------
#     -=           |     x -= 5      |   x = x - 5  |
#----------------------------------------------------
#     *=           |     x *= 5      |   x = x * 5  |
#----------------------------------------------------
#     /=           |     x /= 5      |   x = x / 5  |
#----------------------------------------------------
#     **=          |     x **= 5     |   x = x ** 5 |
#----------------------------------------------------
#     %=           |     x %= 5      |   x = x % 5  |
#----------------------------------------------------
#     //=          |     x //= 5     |   x = x // 5 |
#----------------------------------------------------


